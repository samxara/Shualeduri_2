    {
        //
        $customer = Customers::where('user_id', '=', $id)->first();

        $transactions = Transactions::orderBy('id', 'DESC')
                                    ->where('sender_id', '=',$customer['id'])
                                    ->select('receiver_number', 'amount', 'status','transaction_type','updated_at')
                                    ->paginate(5);
        return $transactions;
    }

